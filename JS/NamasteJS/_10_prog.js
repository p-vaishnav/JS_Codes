// why do we need callbacks?

/*
JS is a event driven programming language
i.e ceratin tasks needs to be completed if a certain event is occured

What are callbacks?
in JS functions are object, function which are passed as a parameter to another function is called a callback
A callback can be normal as well as arrow function

Callbacks make sure that a function is not going to run before a task is completed but will run right after the task has completed.
It helps us develop asynchronous JavaScript code and keeps us safe from problems and errors.

*/